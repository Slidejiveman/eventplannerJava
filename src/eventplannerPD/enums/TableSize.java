package eventplannerPD.enums;

/**
 * An enumeration representing the size of a table.
 * The values grow from 4 to 12 in increments of 2.
 */
public enum TableSize {
    /**
     * The value for a four seat table.
     */
    Four,
    /**
     * The value for a 6 seat table.
     */
    Six,
    /**
     * The value for an 8 seat table.
     */
    Eight,
    /**
     * The value for a 10 seat table.
     */
    Ten,
    /**
     * The value for a 12 seat table.
     */
    Twelve
}