package eventplannerPD.enums;

/**
 * An enumeration representing the shape of a table in the system.
 * 
 * Tables may be either:
 * Square
 * Rectangle
 * LongRectangle
 * Circle
 * Ellipse
 */
public enum TableShape {
    /**
     * The value representing a square table.
     */
    Square,
    /**
     * The value representing a rectangular table.
     */
    Rectangle,
    /**
     * The value representing a long rectangular table.
     */
    LongRectangle,
    /**
     * The value representing a circular table.
     */
    Circle,
    /**
     * The value representing an elliptical table.
     */
    Ellipse
}